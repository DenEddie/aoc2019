        private static int Execute(List<int> intCode, List<int> phases, int input)
        {
            var phaseCombinations = phases.Permute();
            var maxResult = 0;

            foreach (var combination in phaseCombinations)
            {
                var result = input;
                List<IntCodeComputer> amplifiers = combination.Select(p => new IntCodeComputer(intCode, p)).ToList();

                var index = 0;
                while (!amplifiers[index].Halted)
                {
                    result = amplifiers[index].Execute(result);
                    index = index < amplifiers.Count - 1 ? index + 1 : 0;
                }
                if (maxResult < result)
                {
                    maxResult = result;
                }
            }
            return maxResult;
        }

